public class Point {
private int x,y;

public Point(){
    x=0;
    y=0;
    
}
public Point(int x, int y){
    this.x=x;
    this.y=y;
    
}

public void setX(int x){
    this.x=x;
    
}
public void setY(int y){
    this.y=y;
   }

public int getX(){
return x;
}
public int getY(){
return y;

//what this class will do is that it will set and get all the variables that we will use later, thats it nothing more!
}
}
